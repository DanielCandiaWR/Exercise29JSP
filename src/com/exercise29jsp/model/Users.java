package com.exercise29jsp.model;

public class Users {
	private String id;
	private String name;
	
	public Users() {
		
	}
	public Users(String id, String name) {
		
	}
	
	public String getId() {
		return this.id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
	public String getName() {
		return this.name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
